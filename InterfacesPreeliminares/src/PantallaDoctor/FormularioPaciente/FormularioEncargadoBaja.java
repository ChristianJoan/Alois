        int fila=this.jTableBajas.getSelectedRow();
        DefaultTableModel model=(DefaultTableModel)this.jTableBajas.getModel();
        SQL="DELETE FROM ENCARGADO WHERE RFC="+"'"+model.getValueAt(fila,0).toString()+"'";
        conector.baja(SQL);
        
        
